from dataclasses import dataclass
from typing import Tuple


@dataclass(frozen=True)
class Circuit:
    name: str
    country: str
    city: str
    total_laps: int
    circuit_length_km: float
    base_lap_time_s: float          # Pole-position qualifying benchmark (seconds)
    tire_deg_multiplier: float      # 1.0 = Silverstone baseline; >1 = more degradation
    fuel_consumption_kg: float      # Average kg per lap
    overtake_difficulty: float      # 0.0 = Monza (easy), 1.0 = Monaco (near-impossible)
    drs_zones: int
    track_temp_range: Tuple[float, float]  # (min, max) °C on race day
    is_street_circuit: bool
    rain_probability: float         # 0–1 chance of rain during race
    # Compound indices: (Hard#, Medium#, Soft#) where 1=C1-Ultrahard, 6=C6-Ultrasoft
    available_compounds: Tuple[int, int, int]
    safety_car_probability: float   # Per-race probability of at least one SC
    # Sector time fractions (must sum to ~1.0). Used for sector time display and track identity.
    # S1/S2/S3 proportions calibrated from pole lap sector data.
    sector_splits: Tuple[float, float, float] = (0.33, 0.34, 0.33)


